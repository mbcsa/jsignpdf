    public void registerPK11Providers() {
        lblMessage.setText("Detectando token...");
        PKCS11Utils.unregisterProviders();
        PKCS11Utils.registerProviders(ConfigProvider.getInstance().getProperty("pkcs11config.path"));        // TODO add your handling code here:
        final Set<String> tmpKsTypes = KeyStoreUtils.getKeyStores();
        for(String t: tmpKsTypes) {
            System.out.println(t);
        }
        if (tmpKsTypes.contains("PKCS11")) {
            System.out.println("ENcontrado - muestro panel de firma");
            parentForm.showSignPanel(tmpKsTypes);
        } else {
            lblMessage.setText("No se encontr√≥ un dispositivo de firma.");
            System.out.println("NO ENCONTRADO - REINTENTAR");
            this.btnDetectToken.setVisible(true);
        }
    }

